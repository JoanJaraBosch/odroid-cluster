#! /bin/bash


# Carreguem el script network_api.sh com a una llibreria, per 
#	poder fer servir les seves funcions
source network_lib.sh

VERSION="20.02.4"

ip=$1
mask=$2


if [ $(munge -n | unmunge | grep ENCODE_HOST | grep \(0.0.0.0\) | wc -l) -eq 1 ]; then
	apt remove --purge munge -y
	apt install munge -y
	dd if=/home/munge.key of=/etc/munge/munge.key
	systemctl restart munge
	if [ $(munge -n | unmunge | grep ENCODE_HOST | grep \(0.0.0.0\) | wc -l) -eq 1 ]; then
		echo "Could not fix error in munge:
	ENCODE_HOST: ??? (0.0.0.0)

You can check if the problem persists with the command:
	munge -n | unmunge" 2>&1
		exit 1
	fi
fi

# Afegim el usuari slurm, aquest no te directori home ni shell, password no definit
useradd -M slurm --shell /usr/sbin/nologin --home-dir /nonexistent --password "*"

# Instal.lem dependencies
apt-get  install libfreeipmi-dev libhwloc-dev freeipmi libmunge-dev libz-dev -y

cd /usr/local/src/

# Descarreguem, descomprimim i anem al nou directori de slurm, per procedir a la instalació
wget https://download.schedmd.com/slurm/slurm-"$VERSION".tar.bz2
tar --bzip -x -f slurm*tar.bz2 && rm slurm*tar.bz2
cd slurm-"$VERSION"/

# Creem un nou directori on guardarem alguns dels fitxers de configuració
mkdir /usr/local/slurm 
./configure --prefix=/usr/local/slurm

# Compilem i instal.lem slurm
make -j25
make install

mkdir -p /usr/local/slurm/etc/cgroup
cp /usr/local/src/slurm-"$VERSION"/etc/cgroup.conf.example  /usr/local/slurm/etc/cgroup/cgroup.release_common
ln -s /usr/local/slurm/etc/cgroup/cgroup.release_common /usr/local/slurm/etc/cgroup/release_devices
ln -s /usr/local/slurm/etc/cgroup/cgroup.release_common /usr/local/slurm/etc/cgroup/release_cpuset
ln -s /usr/local/slurm/etc/cgroup/cgroup.release_common /usr/local/slurm/etc/cgroup/release_freezer

echo "/dev/null 
/dev/urandom 
/dev/zero 
/dev/cpu/*/* 
/dev/pts/*" > /usr/local/slurm/etc/allowed_devices.conf

echo "# slurm.conf file generated by configurator easy.html.
# Put this file on all nodes of your cluster. 
# See the slurm.conf man page for more information. 
# 
SlurmctldHost=master 
# 
#MailProg=/bin/mail 
MpiDefault=none
#MpiParams=ports=#-# 
ProctrackType=proctrack/cgroup 
ReturnToService=1 
SlurmctldPidFile=/var/run/slurmctld.pid 
#SlurmctldPort=6817 
SlurmdPidFile=/var/run/slurmd.pid
#SlurmdPort=6818
SlurmdSpoolDir=/var/spool/slurmd
SlurmUser=slurm
#SlurmdUser=root
StateSaveLocation=/var/spool/slurm
SwitchType=switch/none
TaskPlugin=task/cgroup
#
#
# TIMERS 
#KillWait=30 
#MinJobAge=300 
#SlurmctldTimeout=120 
#SlurmdTimeout=300 
# 
# 
# SCHEDULING 
SchedulerType=sched/builtin 
SelectType=select/cons_tres 
SelectTypeParameters=CR_Core 
# 
# 
# LOGGING AND ACCOUNTING 
AccountingStorageType=accounting_storage/none 
ClusterName=cluster 
#JobAcctGatherFrequency=30 
JobAcctGatherType=jobacct_gather/none 
#SlurmctldDebug=info 
SlurmctldLogFile=/var/log/slurmctld.log 
#SlurmdDebug=info 
SlurmdLogFile=/var/log/slurmd.log 
# 
# 
# COMPUTE NODES 
NodeName=odroid[1-32] CPUs=4 RealMemory=1727 Sockets=4 CoresPerSocket=1 ThreadsPerCore=1 State=UNKNOWN 
PartitionName=main Nodes=odroid[1-32] Default=YES MaxTime=30 State=UP" > /usr/local/slurm/etc/slurm.conf

echo "CgroupAutomount=yes
CgroupReleaseAgentDir=\"/usr/local/slurm/etc/cgroup\" 
ConstrainCores=yes 
TaskAffinity=yes 
ConstrainDevices=yes 
AllowedDevicesFile=\"/usr/local/slurm/etc/allowed_devices.conf\" 
ConstrainRAMSpace=no" > /usr/local/slurm/etc/cgroup.conf

chown -R slurm: /usr/local/src/slurm-20.02.4/
chown -R slurm: /usr/local/slurm/

if [ $(cat /etc/exports | grep "/usr/local/src/slurm-${VERSION}" | wc -l) -eq 0 ]; then
	echo "/usr/local/src/slurm-${VERSION} $(calculate_network_ip $ip $mask)$(mask_to_cidr $mask)(rw,no_root_squash,no_subtree_check)" >> /etc/exports
fi

if [ $(cat /etc/exports | grep "/usr/local/slurm/etc" | wc -l) -eq 0 ]; then
	echo "/usr/local/slurm/etc $(calculate_network_ip $ip $mask)$(mask_to_cidr $mask)(rw,no_root_squash,no_subtree_check)" >> /etc/exports
fi

exportfs -a

# Creem enllaços simbolics per a poder executar comandes de slurm mitjançant qualsevol usuari
ln -s /usr/local/slurm/sbin/* /usr/sbin/
ln -s /usr/local/slurm/bin/* /usr/bin/

# Creem un nou directori dintre de /var/spool anomenat slurm per a que slurmctld pugui fer-lo servir
mkdir /var/spool/slurm
chown slurm: /var/spool/slurm

# Copiem el slurmctld.service al directori pertinent
cp -ap /usr/local/src/slurm-20.02.4/etc/slurmctld.service /etc/systemd/system/
chown slurm: /etc/systemd/system/slurmctld.service

# Creem el directori de logs
mkdir /var/log/slurm
chown slurm: /var/log/slurm
chmod 664 /var/log/slurm


systemctl enable slurmctld
systemctl start slurmctld
